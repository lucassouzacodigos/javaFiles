public class SomarDoisInteiros{
    
    public double SomarDoisInteiros(double n1, double n2){
        return n1 + n2;
    }
    
    public double SubtrairDoisInteiros(double n2, double n1){
        return n2 - n1;
    }
    
    public double dividir(double n1, double n2){
        return n1/n2;
    }
    
    public double multiplica(double n1, double n2){
        return n1 * n2;
    }
       
}
